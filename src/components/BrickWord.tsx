type GlyphKey = keyof typeof GLYPHS

function advanceOf(ch: string): number {
    if (ch === ' ') return SPACE_ADV
    const g = GLYPHS[ch as GlyphKey]
    return g ? g.w : 0
}

export function BrickWord({ word, height = DEFAULT_HEIGHT }: { word: string; height?: number }) {
    const chars = word.toUpperCase().split('')
    const scale = height / UPM
    const baseline = Math.round((height + PAD_Y - 750 * scale) / 2 + 750 * scale)

    // Pre-compute horizontal offsets so each glyph render is pure.
    const xs: number[] = []
    let cursor = 0
    for (const ch of chars) {
        xs.push(cursor)
        cursor += advanceOf(ch) + LETTER_SPACING
    }

    const svgW = Math.ceil(cursor * scale) + PAD_X * 2
    const svgH = height + PAD_Y

    const glyphEls = chars.map((ch, i) => {
        const g = GLYPHS[ch as GlyphKey]
        if (!g) return null // spaces and any unsupported char just advance the cursor
        const tx = (PAD_X + xs[i] * scale).toFixed(2)
        const transform = `translate(${tx}, ${baseline}) scale(${scale.toFixed(4)}, -${scale.toFixed(4)})`

        return (
            <g key={i}>
                <path d={g.outer} fill="#FFD700" stroke="#FFD700"
                    strokeWidth={STROKE_W * 8} strokeLinejoin="round" transform={transform} />
                <path d={g.outer} fill="black" stroke="black"
                    strokeWidth={STROKE_W * 2} strokeLinejoin="round" transform={transform} />
                <path d={g.inner} fill="white" stroke="none" transform={transform} />
                {'hole' in g && g.hole && (
                    <path d={g.hole} fill="black" stroke="none" transform={transform} />
                )}
                <path d={g.d} fill="none" stroke="black"
                    strokeWidth={STROKE_W * 0.5} strokeLinejoin="round"
                    fillRule="nonzero" transform={transform} />
            </g>
        )
    })

    return (
        <svg
            xmlns="http://www.w3.org/2000/svg"
            viewBox={`0 0 ${svgW} ${svgH}`}
            width={svgW}
            height={svgH}
            style={{ display: 'block' }}
            aria-label={word}
            role="img"
        >
            {glyphEls}
        </svg>
    )
}
